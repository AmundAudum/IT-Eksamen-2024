      </div>
      <div>
        <span class="text-xl text-white">
        åpne cisco packet tracer når du har lastet det ned, og logg inn hvis den spør deg om det. så kan du begyne med å hente ut to pc-er som du finner under knappen nederst til venstre under "end devices" etter det skal du hente ut en switch som du finner under "network devices" og så "switches" så hent ut den første som kommer opp. deretter skal du ta en kabel som heter "copper straight Through" som du finner under conections nederst til venstre og koble sammen pc-ene med switchen som vist på bilde.
