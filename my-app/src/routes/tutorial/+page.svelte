       <b><h1 class=" text-2xl text-white">Steg 3. IP konfiguarsjon/h1</b>
      </div>
      <div>
        <span class="text-xl text-white">
          trykk på den ene pc-en, og trykk på "config", deretter trykk på "interface" så "Fastethernet0" velg at IP Configuration skal være på static og gi den en ip f.eks "192.168.1.1" og under subnet mask gi den 255.255.255.0 deretter gjør det samme på den andre pc-en. men pass på at den ikke har samme IPv4. gi den "192.168.1.2" istedet.
